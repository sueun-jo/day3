int smallest(int a, int b, int c) {
	int min = (a > b) ? b : a;
	return (min > c) ? c : min;
}

int biggest(int a, int b, int c) {
	int max = (a > b) ? a : b; 
	return max > c ? max : c;
}

int main(void)
{
	int x, y, z;
	printf("3개 정수 입력: ");
	scanf_s("%d %d %d", &x, &y, &z);
	
	printf("%d %d %d 중 제일 작은 숫자는 %d, 가장 큰 숫자는 %d입니다.", x, y, z, smallest(x,y,z), biggest(x,y,z));
	
	return 0;
}
	
